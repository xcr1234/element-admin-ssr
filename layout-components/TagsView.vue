<template>
  <div id="tags-view-container" class="tags-view-container">
    <scroll-pane ref="scrollPane" class="tags-view-wrapper" @scroll="handleScroll">
      <nuxt-link v-for="(tag,index) in tags" tag="span" :key="tag.path" :to="tag.path"
        class="tags-view-item" :class="$route.path === tag.path ? 'active' : ''"
            @contextmenu.prevent="openMenu(tag,$event)">
        {{tag.meta.title}}
        <span v-if="!(tag.meta && tag.meta.notClose)" class="el-icon-close" @click.prevent.stop="closeTag(tag,index)"></span>
      </nuxt-link>
<!--      <span class="tags-view-item router-link-exact-active router-link-active active" @contextmenu.prevent="openMenu(null,$event)">文档</span>-->
    </scroll-pane>
    <ul v-show="visible" :style="{left:left+'px',top:top+'px'}" class="contextmenu">
      <li>刷新</li>
      <li>关闭</li>
      <li>关闭其他</li>
      <li>关闭所有</li>
    </ul>
  </div>
</template>

<script>
import ScrollPane from './ScrollPane'
import { mapState } from 'vuex'

export default {
  components: { ScrollPane },
  name: "TagsView",
  computed:{
    ...mapState('app',['tags'])
  },
  data() {
    return {
      visible: false,
      top: 0,
      left: 0,
      selectedTag: null
    }
  },
  watch: {
    visible(value) {
      if (value) {
        document.body.addEventListener('click', this.closeMenu)
      } else {
        document.body.removeEventListener('click', this.closeMenu)
      }
    }
  },
  mounted() {
  },
  methods: {
    openMenu(tag, e) {
      const menuMinWidth = 105
      const offsetLeft = this.$el.getBoundingClientRect().left // container margin left
      const offsetWidth = this.$el.offsetWidth // container width
      const maxLeft = offsetWidth - menuMinWidth // left boundary
      const left = e.clientX - offsetLeft + 15 // 15: margin right

      if (left > maxLeft) {
        this.left = maxLeft
      } else {
        this.left = left
      }

      this.top = e.clientY
      this.visible = true
      this.selectedTag = tag
    },
    closeMenu() {
      this.visible = false
    },
    handleScroll() {
      this.closeMenu()
    },
    closeTag(tag,index){
      this.$emit('close',{tag,index})
    }
  }
}
</script>

